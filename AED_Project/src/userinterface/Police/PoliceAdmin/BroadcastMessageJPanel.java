    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        if(!txtMessage.getText().trim().isEmpty()){
            for(Enterprise ent: network.getEnterpriseDirectory().getEnterpriseList()){
                if(ent instanceof GovernmentEnterprise){
                    for(Organization org : ent.getOrganizationDirectory().getOrganizationList()){
                        if(org instanceof UserOrganization){

                            BroadcastWorkRequest broadcastWorkRequest = new BroadcastWorkRequest();
                            broadcastWorkRequest.setMessage(txtMessage.getText());
                            broadcastWorkRequest.setSender(account);
                            broadcastWorkRequest.setTimestamp(new Date());
                            
                            org.getWorkQueue().getWorkRequestList().add(broadcastWorkRequest);
                            enterprise.getWorkQueue().getWorkRequestList().add(broadcastWorkRequest);
                            txtMessage.setText("");
                            populateTable();
                        }
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please enter the message");
        }
    }//GEN-LAST:event_btnSendActionPerformed

