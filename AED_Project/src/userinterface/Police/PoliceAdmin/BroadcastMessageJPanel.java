    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblMessages.getModel();
        model.setRowCount(0);
        
        for(WorkRequest req: enterprise.getWorkQueue().getWorkRequestList()){
            if(req instanceof BroadcastWorkRequest){
                
                BroadcastWorkRequest broadcastWorkRequest = (BroadcastWorkRequest) req;
                Object[] row = new Object[2];
                row[0] = broadcastWorkRequest.getTimestamp();
                row[1] = broadcastWorkRequest.getMessage().toString();

                model.addRow(row);
            }
        }
    }
    
